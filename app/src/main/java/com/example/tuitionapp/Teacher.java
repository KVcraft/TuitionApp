package com.example.tuitionapp;

public class Teacher {
    public String firstName, lastName, nic, email, contact, address, course;

    public Teacher(String firstName, String lastName, String nic, String email, String contact, String address, String course) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.nic = nic;
        this.email = email;
        this.contact = contact;
        this.address = address;
        this.course = course;
    }
}

